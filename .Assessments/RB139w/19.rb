What are RVM and Rbenv? What do they enable? Why may they be needed?

=end


=begin

RVM (Ruby enVironment Manager) and Rbenv are Ruby Version Managers for the Ruby language. They allow for the management and use of multiple versions on the same machine to create project environments that may depend on different versions of the language and not conflict. The Ruby language has and is still growing and changing. Past projects might rely on an earlier vresion of the language, while future projects will likely use a more recent version of the language. Though the language is "the same", not all versions have the same implementation and may not be backwards- or forwards-compatible.

=end